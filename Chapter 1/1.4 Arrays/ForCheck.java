/* serves as a test bed for loops made with the for command




 */

public class ForCheck {


    public static void main(String[] args) {
        // Setup two for loops without parenthesis
        for (int m = 0; m < 5; m++)
            for (int n = 0; n < 5; n++)
                System.out.println("This is outer loop run " + m + " and inner loop " + n + ".");


    }

}
